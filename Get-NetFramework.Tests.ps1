        # ----- Finds 3.0

        
    }

    Context Output {
        
        # ----- Finds 4.5 and above
        It "Should enter 4.0 if statement" {
            Mock -CommandName Test-Path -MockWith { 
                Return $True
            }

            Mock -CommandName Get-Item
        }
    }
}
